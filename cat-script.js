let allProductsHTML = '';

// Function to render products
const displayProducts = (products) => {
  let productsHTML = '';

  products.forEach((product) => {
    productsHTML += `
      <div class="products" data-product-id="${product.id}">
      <a href ='product-page.html'>
        <div class="products-image">
          <img src="${product.Image1}" alt="${product.name}">
        </div>
        <div class="products-name">
          <h4>${product.name}</h4>
        </div>
        <div class="products-price">
          <p>$${product.price}</p>
        </div>
        </a>
      </div>
    `;
  });

  // Update the products container with the new HTML
  document.querySelector('.js-products-categories').innerHTML = productsHTML;

  // Add click event listener to each product to save it in localStorage
  const productElements = document.querySelectorAll('.products');
  productElements.forEach((productElement) => {
    productElement.addEventListener('click', () => {
      const productId = productElement.getAttribute('data-product-id');
      const selectedProduct = products.find((p) => p.id == productId);

      // Save the selected product details to localStorage
      localStorage.setItem('selectedProduct', JSON.stringify(selectedProduct));

      // Redirect to product details page
      window.location.href = 'product-page.html';  
    });
  });
};

// Initially render all products
displayProducts(allProducts);

// Get the container for categories and header
const categoriesContainer = document.querySelector('.categories');
const categoryHeader = document.querySelector('.categories-header h1');

// Function to set and display categories
const setCategories = () => {
  const allCategories = allProducts.map(item => item.category);
  
  const categories = [
    "All",
    ...allCategories.filter((item, i) => allCategories.indexOf(item) === i),
  ];

  // Generate and display category options
  categoriesContainer.innerHTML = categories.map(category =>
    `
      <p class="category">${category}</p>
    `).join('');

  // Add event listener for category filtering
  categoriesContainer.addEventListener('click', (e) => {
    const selectedCategory = e.target.textContent;

    // Update the header to reflect the selected category
    categoryHeader.textContent = selectedCategory;

    // Display all products or filter by category
    selectedCategory === "All" 
      ? displayProducts(allProducts) 
      : displayProducts(allProducts.filter(item => item.category === selectedCategory));
  });
};

// Call the function to set up categories
setCategories();

